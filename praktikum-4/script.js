//1. deklarasi variabel string
console.log('--------- Tipe Data Sitring-----------')
let nama = 'Maklon';
let jk = 'Laki-Laki';
console.log('Nama: ',nama);
console.log('Jenis Kelamin: ',jk);

console.log('--------- Tipe Data Number-----------')
//2. deklarasi variabel angka (number)
let umur = 37;
let harga = 2300.50;
console.log('Umur saya: ',umur);
console.log('Harga barang: ', harga);

console.log('--------- Tipe Data Boolean-----------')
//3. deklarasi variabel Boolean (True/False)
let isActive = false;
console.log('Nilai:',isActive);

console.log('--------- Tipe Data Array-----------')
// deklarasi array (daftar)
let buah =['Apel', 'Jeruk', 'Mangga', 'alpukat']; //index array dimulai dari 0 ... n
console.log(buah[3])

console.log('--------- Tipe Data Objek-----------')
//5. deklarasi Object (Data Berstruktur) 
let orang = {
    nama : 'Maklon Frare',
    umur: 32,
    pekerjaan: 'Software Engineering Teacher'
}
console.log(orang.pekerjaan);