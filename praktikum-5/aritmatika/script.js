// 1. Operator Aritmatika
console.log('1. Operator Aritmatika');
let a = 10;
let b = 3;
console.log('Jumlah: ',a+b); //jumlah
console.log('Kurang: ',a-b); //kurang
console.log('Kali: ', a*b); //kali
console.log('Bagi: ',a/b); //bagi
console.log('Sisa Bagi: ', a%b); //mod (sisa hasil bagi)
console.log('pangkat: ',a**b);//pangkat

//2. Operator Perbandingan
console.log('2. Operator Perbandingan');
console.log(5 == '5'); //true (karena hanya membandingkan nilai)
console.log(5 === '5'); //false (karena tipe data berbeda)
console.log(10 > 5); // true
console.log(10 < 5); // flase
console.log(10 !== '10');   // true

//3. Operator Logika
console.log('3. Operator Logika');
let x = 10;
let y = 5;
console.log(x>5 && y<10);
console.log(x>5 && y>10);
console.log(!(x>5));

