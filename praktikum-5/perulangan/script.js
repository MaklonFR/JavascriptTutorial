//a) Perulangan dengan for Loop
console.log('a) for Loop')

for (let i = 1; i<=5; i++) {
    console.log('Perulangan ke-'+ i);
}

//b) Perulangan while Loop
console.log('b) while Loop');

let i = 1;

while (i <= 5) {
    console.log('Perulangan ke-'+ i);
    i++; //i tambah 1
}

// c) Perulangan dengan do while Loop
console.log('c) do while Loop');
let j = 1;

do {
    console.log('Perulangan ke-' + j);
    j++; // j tambah 1
} while (j <= 10);
